    expect(IpFilterRepository).toHaveBeenCalledWith('sqlite database');
    expect(ScanModeRepository).toHaveBeenCalledWith('sqlite database');
    expect(NorthConnectorRepository).toHaveBeenCalledWith('sqlite database');
